#include <iostream>
#include "Map.hpp"

Map::Map() {}

Map::~Map() {}

char Map::at(int x, int y)
{
	return _map[y][x];
}

int Map::getLevel()
{
	return _currentWorkingLevel;
}

void Map::levelUp()
{
	_currentWorkingLevel++;
}

size_t Map::height()
{
	return _map.size();
}

size_t Map::width()
{
	return _map[0].size();
}

void Map::printMap()
{
	for (size_t y = 0; y < _map.size(); y++)
	{
		for (size_t x = 0; x < _map[0].size(); x++)
			std::cout << _map[y][x];

		std::cout << std::endl;
	}
}

